#Operations on SETS

set1 = {"Orange","Apple","Mango","Grape","Banana","Pine Apple"}
print("The set items are --> ",set1)

set1.pop() #--> removes the 1st element in the set
print("The updated set is  --> ",set1)

set1.add("Avacado") #--> Add's the particular item to the set
print("The updated set is --> ",set1)

set1.remove("Apple") #--> 
print("The updated set is --> ",set1)

set2 = set1.copy()
print("The set1 is --> ",set1)
print("Set1 is copied into SET2 --> ",set2)

